package com.blackjade.crm.model;

public class Wallet {
	private int cid;
	private int cgid;
	private int pnsid;
	private int pnsgid;
	private int balance;
	private int margin;
	private int freeMargin;
	private int prebalan;
	private int changeBalan;
	private int pnl;
	
	private int quantity;

	public int getCid() {
		return cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	public int getCgid() {
		return cgid;
	}

	public void setCgid(int cgid) {
		this.cgid = cgid;
	}

	public int getPnsid() {
		return pnsid;
	}

	public void setPnsid(int pnsid) {
		this.pnsid = pnsid;
	}

	public int getPnsgid() {
		return pnsgid;
	}

	public void setPnsgid(int pnsgid) {
		this.pnsgid = pnsgid;
	}

	public int getBalance() {
		return balance;
	}

	public void setBalance(int balance) {
		this.balance = balance;
	}

	public int getMargin() {
		return margin;
	}

	public void setMargin(int margin) {
		this.margin = margin;
	}

	public int getFreeMargin() {
		return freeMargin;
	}

	public void setFreeMargin(int freeMargin) {
		this.freeMargin = freeMargin;
	}

	public int getPrebalan() {
		return prebalan;
	}

	public void setPrebalan(int prebalan) {
		this.prebalan = prebalan;
	}

	public int getChangeBalan() {
		return changeBalan;
	}

	public void setChangeBalan(int changeBalan) {
		this.changeBalan = changeBalan;
	}

	public int getPnl() {
		return pnl;
	}

	public void setPnl(int pnl) {
		this.pnl = pnl;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	
	
}
